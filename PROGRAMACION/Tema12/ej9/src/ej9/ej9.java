    private void jButtonObrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrirActionPerformed
        // TODO add your handling code here:
        int seleccion = jFileChooserVentanaFicheros.showOpenDialog(this);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File fichero = jFileChooserVentanaFicheros.getSelectedFile();
            try {
                BufferedReader in = new BufferedReader(new FileReader(fichero.getAbsolutePath()));
                String frase;
                frase = in.readLine();
                while (frase != null) {
                    jTextAreares.setText(jTextAreares.getText() + frase + "\n");
                    frase = in.readLine();
                }
                in.close();
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al leer fichero");

            }
        }
        jLabel2.setText((String.valueOf(jSlider1.getValue())));
    }//GEN-LAST:event_jButtonObrirActionPerformed

    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        int seleccion = jFileChooserVentanaFicheros.showSaveDialog(this);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File fichero = jFileChooserVentanaFicheros.getSelectedFile();
            try {
                FileWriter nue = new FileWriter(fichero, true);

                nue.write(jTextAreares.getText() + "\n");

                nue.close();
                if (fichero.exists()) {
                    JOptionPane.showMessageDialog(this, "Fichero creado correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al leer fichero");

            }
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

